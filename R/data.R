#' Phenotypes data
#'
#' A data set containing mouse phenotypes along with tissue, sex, age, and mouse line.
"phenotypes"

#' Phenotypes tissues
#'
#' A data set containing the tissues available for each mouse phenotype.
"phenotype_tissue"

#' Gene expressions genes
#'
#' A data set containing the genes in the gene expressions data.
"gene_expression_genes"

# Gene expressions mouse lines
#'
#' A data set containing the mouse lines in the gene expressions data.
"gene_expression_mouse_lines"

# Gene expressions tissues
#'
#' A data set containing the tissues in the gene expressions data.
"gene_expression_tissues"

#' Gene expressions mouse line tissues
#'
#' A data set containing the tissues available for each mouse line.
"gene_expressions_mouse_line_tissues"

#' Nanostring data
#'
#' A data set containing the correlation of nanostring log fold-change and AMP-AD human gene log fold-change, by module, model, sex, and age group.
"nanostring"

#' Nanostring plotting data
#'
#' A version of the data in \code{\link{nanostring}} specifically designed for plotting via \code{\link{magora_corrplot}}
"nanostring_for_plot"
